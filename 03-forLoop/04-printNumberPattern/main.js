// หมายเหตุ : คำสั่งขึ้นบรรทัดใหม่ใน console ให้ใช้ \n
let str ='' ;

for (let i = 1; i <= 8; i++) {
    // 1st i=1
    for (let j = 1; j <=8; j++) {
        if (j <= i) str += j
    }
    str = str + '\n' 
}
console.log(str);

